use secp256k1::PublicKey;

fn main() {
    // Create a valid compressed public key (0x02 prefix)
    let mut pubkey_bytes = [0u8; 33];
    pubkey_bytes[0] = 0x02;
    // This is a known valid compressed public key X coordinate
    pubkey_bytes[1..].copy_from_slice(&[
        0x79, 0xbe, 0x66, 0x7e, 0xf9, 0xdc, 0xbb, 0xac,
        0x55, 0xa0, 0x62, 0x95, 0xce, 0x87, 0x0b, 0x07,
        0x02, 0x9b, 0xfc, 0xdb, 0x2d, 0xce, 0x28, 0xd9,
        0x59, 0xf2, 0x81, 0x5b, 0x16, 0xf8, 0x17, 0x98
    ]);
    
    match PublicKey::from_slice(&pubkey_bytes) {
        Ok(pk) => println!("Valid public key created"),
        Err(e) => println!("Error: {:?}", e),
    }
}
